//Age variable
let myAge = 31;
//Early years variable
let earlyYears = 2;
//Early years are 10.5 human years
earlyYears = earlyYears * 10.5;
//Early years accounted for
laterYears = myAge - 2;
// Multiply laterYears by 4 and = itself together
laterYears *= 4;
//Age in dog years
myAgeInDogYears = earlyYears + laterYears;
//Name in lower case
let myName = 'robert'.toLowerCase();
//Log output of variables
console.log(`My name is ${myName}. I am ${myAge} years old in human years which is
 ${myAgeInDogYears} in dog years.`);